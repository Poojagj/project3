package practice;

public class swapNos {

	public static void main(String[] args) {
		System.out.println("Swapping numbers a= 20 , b = 30");
		
		int a = 20;
		int b =30;
		int temp;
		
		temp=a;//20
		a=b;//30
		b=temp;//20
		
		System.out.println("a is : " +a);
		System.out.println("b is : " +b);
	}

}
