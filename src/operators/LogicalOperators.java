package operators;

public class LogicalOperators {

	public static void main(String[] args) {
	
		int a = 10, b = 20, c = 20;

		// && operator
		
				System.out.println(a < b && b == c);
			
      // || operator
				
				System.out.println(a < b || b != c);
			
	// ! operator
				
				System.out.println(!(a<b)); // returns false
				System.out.println(!(a>b));  // returns true
				
				
				
				
	}

}
