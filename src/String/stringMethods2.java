package String;

public class stringMethods2 {

	public static void main(String[] args) {
		
		//endsWith() method
		String s="java morning session";  
		System.out.println(s.endsWith("n"));  
		System.out.println(s.endsWith("session"));  
		System.out.println(s.endsWith("morning"));  
		
		System.out.println("");
		
		
		//startsWith() method
		String str="java morning session";  
		System.out.println(str.startsWith("j"));
		System.out.println(str.startsWith("m"));
		System.out.println(str.startsWith("java"));  
		
		System.out.println("");		
	  

		
	}
}
